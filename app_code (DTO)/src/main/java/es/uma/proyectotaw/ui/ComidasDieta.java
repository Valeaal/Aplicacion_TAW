package es.uma.proyectotaw.ui;

public class ComidasDieta {

    private Integer desayuno;
    private Integer mediamanana;
    private Integer comida;
    private Integer cena;
    private Integer merienda;

    public Integer getDesayuno() {
        return desayuno;
    }

    public void setDesayuno(Integer desayuno) {
        this.desayuno = desayuno;
    }

    public Integer getMediamanana() {
        return mediamanana;
    }

    public void setMediamanana(Integer mediamanana) {
        this.mediamanana = mediamanana;
    }

    public Integer getComida() {
        return comida;
    }

    public void setComida(Integer comida) {
        this.comida = comida;
    }

    public Integer getCena() {
        return cena;
    }

    public void setCena(Integer cena) {
        this.cena = cena;
    }

    public Integer getMerienda() {
        return merienda;
    }

    public void setMerienda(Integer merienda) {
        this.merienda = merienda;
    }
}
